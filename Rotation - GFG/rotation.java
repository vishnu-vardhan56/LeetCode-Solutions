class Solution {
    int findKRotation(int arr[], int n) {
         int l = 0;
        int r=arr.length-1;
        while(l<r){
            int mid = (l+r)/2;
            if(arr[mid]>=arr[r]){
                l = mid+1;
            }
            else{
                r = mid;
            }
        }
        return l;
    }
}